package Lab02;

public class Calculator {

    public double CalcSum (double a, double b)
    {
      return a + b;
    }
    public long CalcSum (long a, long b)
    {
        return a + b;
    }
    public int CalcSum (int a, int b)
    {
        return a + b;
    }
    public double CalcDiv (double a, double b)
    {
        return a / b;
    }
    public double CalcDiv (long a, long b)
    {
        return (double) a / b;
    }
    public double CalcDiv (int a, int b)
    {
        return (double) a / b;
    }
    public double CalcMulti (double a, double b)
    {
        return a * b;
    }
    public long CalcMulti (long a, long b)
    {
        return a * b;
    }
    public int CalcMulti (int a, int b)
    {
        return a * b;
    }
    public double CalcSubstr (double a, double b)
    {
        return a - b;
    }
    public long CalcSubstr (long a, long b)
    {
        return a - b;
    }
    public int CalcSubstr (int a, int b)
    {
        return a - b;
    }
}
